public class Pacote {
    private double weight;
    private double width;
    private double height;
    private double length;

    public Pacote(double weight, double width, double height, double length) {
        this.weight = weight;
        this.width = width;
        this.height = height;
        this.length = length;
    }

    public double getWeight() {
        return weight;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }


    public double getLength() {
        return length;
    }

}
